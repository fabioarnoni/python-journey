# Sequence Operations

string1 = "He's "
string2 = "Probably "
string3 = "pining "
string4 = "for the "
string5 = "fjords"

print(string1 + string2 + string3 + string4 + string5)

print("Hello " * 5)
print("Hello " * (5 + 4))
print("Hello " * 5 + "4")

