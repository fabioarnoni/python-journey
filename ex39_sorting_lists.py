# Sorting Lists

numbers = [2, 4, 6, 8, 1, 3, 5, 7, 9]
another_numbers = numbers

print(numbers)
numbers.sort()
print(numbers)
numbers.sort(reverse=True)
print(numbers)
print(another_numbers)

